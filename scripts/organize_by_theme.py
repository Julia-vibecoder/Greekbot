def normalize_greek(word):
    """Remove accents and lowercase."""
    nfkd = unicodedata.normalize('NFD', word.lower())
    return ''.join(c for c in nfkd if unicodedata.category(c) != 'Mn')


def extract_stem(greek_word):
    """Extract stem from a Greek word for cognate matching."""
    word = normalize_greek(greek_word.strip())
    for article in ["ο ", "η ", "το ", "οι ", "τα ", "τον ", "την ", "τους ", "τις "]:
        if word.startswith(article):
            word = word[len(article):]
            break
    first_word = word.split()[0] if word.split() else word
    if len(first_word) < 3:
        return first_word
    stem = first_word
    for prefix in sorted(GREEK_PREFIXES, key=len, reverse=True):
        np = normalize_greek(prefix)
        if stem.startswith(np) and len(stem) > len(np) + 2:
            stem = stem[len(np):]
            break
    for ending in ["ηση", "αση", "ωση", "ισμος", "ισμο", "ιστης", "ιστη",
                    "οτητα", "ητα", "ικος", "ικη", "ικο", "ιος", "ια", "ιο",
                    "ος", "ης", "ες", "ον", "ων", "ου", "ει", "αι",
                    "ομαι", "ουμε", "ετε", "ουν", "ω"]:
        ne = normalize_greek(ending)
        if stem.endswith(ne) and len(stem) > len(ne) + 2:
            stem = stem[:len(stem) - len(ne)]
            break
    if len(stem) >= 3:
        return stem
    return first_word[:min(4, len(first_word))]


def get_first_greek_word(text):
    text = text.strip()
    for article in ["τα ", "τον ", "την ", "τους ", "τις ", "το ", "οι ", "ο ", "η "]:
        if text.lower().startswith(article):
            text = text[len(article):]
            break
    return text.split()[0] if text.split() else text


# ── Theme assignment ──────────────────────────────────────────────────────

def assign_theme(row):
    """Assign a thematic group to a row."""
    greek = row.get("greek", "").lower()
    russian = row.get("russian", "").lower()
    english = row.get("english", "").lower()
    category = row.get("category", "").strip()

    # 1. Conversational categories
    if category in CONVERSATIONAL_CATEGORIES:
        return "Разговорные фразы"

    # 2. Society categories
    if category in SOCIETY_CATEGORIES:
        return "Политика и общество"

    # 3. "Фразы уровня Γ2" — route based on content or to abstract
    if category == "Фразы уровня Γ2":
        # Try all themes
        best_theme = None
        best_score = 0
        for theme, keywords in THEMES.items():
            if theme == "Разговорные фразы":
                continue
            score = 0
            for kw in keywords.get("ru", []):
                if kw in russian:
                    score += 2
            for kw in keywords.get("en", []):
                if kw in english:
                    score += 1.5
            for kw in keywords.get("gr", []):
                if kw in greek:
                    score += 1
            if score > best_score:
                best_score = score
                best_theme = theme
        if best_score > 0:
            return best_theme
        return "Абстрактные понятия"

    # 4. Keyword matching across all themes
    best_theme = "Прочее"
    best_score = 0

    for theme, keywords in THEMES.items():
        if theme == "Разговорные фразы":
            continue
        score = 0
        for kw in keywords.get("ru", []):
            if kw in russian:
                score += 2
        for kw in keywords.get("en", []):
            if kw in english:
                score += 1.5
        for kw in keywords.get("gr", []):
            if kw in greek:
                score += 1
        if score > best_score:
            best_score = score
            best_theme = theme

    # If nothing matched at all, check if it's a phrase
    if best_score == 0:
        if not english.strip() and len(greek.split()) > 3:
            return "Разговорные фразы"
        return "Прочее"

    return best_theme


# ── Cognate grouping ─────────────────────────────────────────────────────

def find_cognate_groups(entries):
    stem_to_entries = defaultdict(list)
    entry_stems = {}
    for i, entry in enumerate(entries):
        stem = extract_stem(entry.get("greek", ""))
        if len(stem) >= 3:
            entry_stems[i] = stem
            stem_to_entries[stem].append(i)

    normalized_greeks = {}
    for i, entry in enumerate(entries):
        normalized_greeks[i] = normalize_greek(get_first_greek_word(entry.get("greek", "")))

    parent = list(range(len(entries)))

    def find(x):
        while parent[x] != x:
            parent[x] = parent[parent[x]]
            x = parent[x]
        return x

    def union(x, y):
        px, py = find(x), find(y)
        if px != py:
            parent[px] = py

    for stem, indices in stem_to_entries.items():
        if len(indices) > 1:
            for idx in indices[1:]:
                union(indices[0], idx)

    stems_list = [(i, s) for i, s in entry_stems.items() if len(s) >= 4]
    for i, stem_i in stems_list:
        norm_i = normalized_greeks.get(i, "")
        for j, stem_j in stems_list:
            if i >= j:
                continue
            norm_j = normalized_greeks.get(j, "")
            min_len = min(len(stem_i), len(stem_j))
            if min_len >= 4 and stem_i[:min_len] == stem_j[:min_len]:
                union(i, j)
            elif len(stem_i) >= 4 and stem_i in norm_j:
                union(i, j)
            elif len(stem_j) >= 4 and stem_j in norm_i:
                union(i, j)

    groups = defaultdict(list)
    for i in range(len(entries)):
        groups[find(i)].append(i)
    return {k: v for k, v in groups.items() if len(v) >= 2}


# ── Main ─────────────────────────────────────────────────────────────────

def main():
    print("Reading CSV...")
    rows = []
    with open(INPUT_FILE, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for row in reader:
            # Skip theme separator rows from previous organization
            if row.get("greek", "").strip() == "---":
                continue
            rows.append(row)

    total_input = len(rows)
    print(f"Read {total_input} entries")

    print("Assigning themes...")
    for row in rows:
        row["_theme"] = assign_theme(row)

    theme_groups = defaultdict(list)
    for row in rows:
        theme_groups[row["_theme"]].append(row)

    theme_order = [
        "Политика и общество",
        "Право и закон",
        "Экономика и финансы",
        "Работа и карьера",
        "Образование",
        "Медицина и здоровье",
        "Психология и эмоции",
        "Семья и отношения",
        "Быт и повседневность",
        "Природа и окружающая среда",
        "Культура и искусство",
        "Язык и коммуникация",
        "Религия и философия",
        "Военное дело",
        "География и путешествия",
        "Спорт и тело",
        "Строительство и жильё",
        "Технологии",
        "Еда и напитки",
        "Одежда и внешность",
        "Морское дело",
        "Этика и ценности",
        "Разговорные фразы",
        "Действия и процессы",
        "Абстрактные понятия",
        "Прочее",
    ]

    print("Finding cognate groups and sorting...")
    total_cognate_groups = 0
    output_rows = []

    for theme in theme_order:
        entries = theme_groups.get(theme, [])
        if not entries:
            continue

        cognates = find_cognate_groups(entries)
        total_cognate_groups += len(cognates)

        in_cognate = set()
        for group_indices in cognates.values():
            for idx in group_indices:
                in_cognate.add(idx)

        cognate_group_list = []
        for root_idx, indices in cognates.items():
            group_entries = [entries[i] for i in indices]
            group_entries.sort(key=lambda r: r.get("greek", "").lower())
            sort_key = group_entries[0].get("greek", "").lower()
            cognate_group_list.append((sort_key, group_entries))
        cognate_group_list.sort(key=lambda x: x[0])

        singles = [entries[i] for i in range(len(entries)) if i not in in_cognate]
        singles.sort(key=lambda r: r.get("greek", "").lower())

        output_rows.append({
            "greek": "---", "russian": theme,
            "english": "", "example": "", "category": "",
        })

        for _, group in cognate_group_list:
            output_rows.extend(group)
        output_rows.extend(singles)

    data_rows = [r for r in output_rows if r.get("greek") != "---"]
    total_output = len(data_rows)

    if total_output != total_input:
        print(f"WARNING: Entry count mismatch! Input: {total_input}, Output: {total_output}")
    else:
        print(f"Entry count verified: {total_output} entries (matches input)")

    print("Writing output...")
    fieldnames = ["greek", "russian", "english", "example", "category"]
    with open(INPUT_FILE, "w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames, extrasaction="ignore")
        writer.writeheader()
        for row in output_rows:
            writer.writerow({k: row.get(k, "") for k in fieldnames})

    print("\n" + "=" * 60)
    print("STATISTICS")
    print("=" * 60)
    for theme in theme_order:
        count = len(theme_groups.get(theme, []))
        if count > 0:
            print(f"  {theme}: {count}")
    print(f"\n  Total cognate groups found: {total_cognate_groups}")
    print(f"  Total entries: {total_output}")
    print("=" * 60)


if __name__ == "__main__":
    main()
